package Modelo;

public class Horario {
    
    int id;
    String iniHorario;
    String finHorario;
    String diaHorario;

    public Horario() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIniHorario() {
        return iniHorario;
    }

    public void setIniHorario(String iniHorario) {
        this.iniHorario = iniHorario;
    }

    public String getFinHorario() {
        return finHorario;
    }

    public void setFinHorario(String finHorario) {
        this.finHorario = finHorario;
    }

    public String getDiaHorario() {
        return diaHorario;
    }

    public void setDiaHorario(String diaHorario) {
        this.diaHorario = diaHorario;
    }
    
    
    
}
