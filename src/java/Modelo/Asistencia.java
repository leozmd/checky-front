package Modelo;

public class Asistencia {
    int id;
    String fecAsistencia;
    String edoAsistencia;
    int idHorario;
    int idPeriodo;
    int idUsuario;
    String nomUsuario;
    String appatUsuario;
    String apmatUsuario;

    public Asistencia() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFecAsistencia() {
        return fecAsistencia;
    }

    public void setFecAsistencia(String fecAsistencia) {
        this.fecAsistencia = fecAsistencia;
    }

    public String getEdoAsistencia() {
        return edoAsistencia;
    }

    public void setEdoAsistencia(String edoAsistencia) {
        this.edoAsistencia = edoAsistencia;
    }

    public int getIdHorario() {
        return idHorario;
    }

    public void setIdHorario(int idHorario) {
        this.idHorario = idHorario;
    }

    public int getIdPeriodo() {
        return idPeriodo;
    }

    public void setIdPeriodo(int idPeriodo) {
        this.idPeriodo = idPeriodo;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getNomUsuario() {
        return nomUsuario;
    }

    public void setNomUsuario(String nomUsuario) {
        this.nomUsuario = nomUsuario;
    }

    public String getAppatUsuario() {
        return appatUsuario;
    }

    public void setAppatUsuario(String appatUsuario) {
        this.appatUsuario = appatUsuario;
    }

    public String getApmatUsuario() {
        return apmatUsuario;
    }

    public void setApmatUsuario(String apmatUsuario) {
        this.apmatUsuario = apmatUsuario;
    }
    
    
}
