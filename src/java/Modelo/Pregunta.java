package Modelo;

public class Pregunta {
    int id;
    int idClase;
    String txtPregunta;
    String statPregunta;
    int idUsuario;
    String nomUsuario;
    String appatUsuario;
    String apmatUsuario;

    public Pregunta() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdClase() {
        return idClase;
    }

    public void setIdClase(int idClase) {
        this.idClase = idClase;
    }

    public String getTxtPregunta() {
        return txtPregunta;
    }

    public void setTxtPregunta(String txtPregunta) {
        this.txtPregunta = txtPregunta;
    }

    public String getStatPregunta() {
        return statPregunta;
    }

    public void setStatPregunta(String statPregunta) {
        this.statPregunta = statPregunta;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getNomUsuario() {
        return nomUsuario;
    }

    public void setNomUsuario(String nomUsuario) {
        this.nomUsuario = nomUsuario;
    }

    public String getAppatUsuario() {
        return appatUsuario;
    }

    public void setAppatUsuario(String appatUsuario) {
        this.appatUsuario = appatUsuario;
    }

    public String getApmatUsuario() {
        return apmatUsuario;
    }

    public void setApmatUsuario(String apmatUsuario) {
        this.apmatUsuario = apmatUsuario;
    }
    
    
}
