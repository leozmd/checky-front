package Modelo;

public class Turno {
    int id;
    String nomTurno;
    int numClases;

    public Turno() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomTurno() {
        return nomTurno;
    }

    public void setNomTurno(String nomTurno) {
        this.nomTurno = nomTurno;
    }

    public int getNumClases() {
        return numClases;
    }

    public void setNumClases(int numClases) {
        this.numClases = numClases;
    }
    
    
}
