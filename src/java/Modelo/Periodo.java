package Modelo;

public class Periodo {
    int id;
    String nomPeriodo;
    String iniPeriodo;
    String finPeriodo;

    public Periodo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomPeriodo() {
        return nomPeriodo;
    }

    public void setNomPeriodo(String nomPeriodo) {
        this.nomPeriodo = nomPeriodo;
    }

    public String getIniPeriodo() {
        return iniPeriodo;
    }

    public void setIniPeriodo(String iniPeriodo) {
        this.iniPeriodo = iniPeriodo;
    }

    public String getFinPeriodo() {
        return finPeriodo;
    }

    public void setFinPeriodo(String finPeriodo) {
        this.finPeriodo = finPeriodo;
    }
    
    
}
